function movePlayer() {
	if (kb.pressing('left')) player.vel.x = -5;
	else if (kb.pressing('right')) player.vel.x = 5;
	else player.vel.x = 0;

	if (kb.pressing('up')) player.vel.y = -5;
	else if (kb.pressing('down')) player.vel.y = 5;
	else player.vel.y = 0;
}
